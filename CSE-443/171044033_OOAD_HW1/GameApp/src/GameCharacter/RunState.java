package GameCharacter;

/**
 * Run state handler for character
 * I change the graphic according to the run state here in the character class.
 * This gives the effect of running.
 */
public enum RunState
{
    /**
     * STOP STATE
     */
    STOP,

    /**
     * RUN STATE
     */
    RUN1,

    /**
     * MORE RUN STATE
     */
    RUN2
}
